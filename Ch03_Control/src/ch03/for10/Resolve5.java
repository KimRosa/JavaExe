package ch03.for10;



public class Resolve5 {
    /*
     *  5. do~while문을 이용해서 10부터 20까지 출력하세요
     */

	  public static void main(String[] args) {
	        int number = 10;

	        do {
	            System.out.println(number);
	            number++;
	        } while (number <= 20);
	    }
	    
	}