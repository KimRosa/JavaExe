package ch03.for10;



public class Resolve5 {
    /*
     *  do~while문을 이용해서 1부터 100까지 출력하세요
     */

	  public static void main(String[] args) {
	        int number = 10;

	        do {
	            System.out.println(number);
	            number++;
	        } while (number <= 20);
	    }
	    
	}